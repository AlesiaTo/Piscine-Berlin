char	*ft_remove_extra(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		while (str[i] == ' ' || str[i] == ':')
			i++;
		j = i;
		while (str[i] != '\n')
			i++;
		str[i] = '\0';
	}
	return (&str[j]);
}
